public class pertemuan22 {
    public static void main(String[] args) {
        int n = 15;
        for(int i=1;i<=n;i++){
            System.out.print((i+(i-1))+ " ");
        }
        System.out.println("");
        for(int i=1;i<=n;i++){
            System.out.print((i*2)+ " ");
        }
    }
}
